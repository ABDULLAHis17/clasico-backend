class Friend {
  final String id;
  final String username;
  final String profileImage; // Player emoji/image representing avatar
  final String favoriteTeam;
  final String favoriteTeamLogo;
  final String favoritePlayer;
  final String favoritePlayerImage;
  final String nationalTeam;
  final String nationalTeamFlag;

  Friend({
    required this.id,
    required this.username,
    required this.profileImage,
    required this.favoriteTeam,
    required this.favoriteTeamLogo,
    required this.favoritePlayer,
    required this.favoritePlayerImage,
    required this.nationalTeam,
    required this.nationalTeamFlag,
  });
}
